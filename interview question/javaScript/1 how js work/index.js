// Episode 1 : Execution Context 

// Everything in JS happens inside the 
// Execution context  

// Memory component(Variable Environment)  | Code Component(Thread of Execution)
            // key : value                      ---------------
            // a : 10                           ---------------
            // function:{...}                   ---------------

 

// In the container the first component is Memory component and the 
// 2nd one is code component.

// Memory component has all the variables and functions in key value 
// pairs.It is also called Variable environment

// Code component is the place where code is executed one line at a time.
// It is also called the Thread of Execution.

// JS is a synchronous,single-threaded language
// Synchronous : In a specific synchronous order.
// Single-threaded: One command at a time.